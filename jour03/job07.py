def determiner_specialisation(langage):
    if langage == "JavaScript":
        print("Tu es un développeur web")
    elif langage == "python":
        print("Tu es un développeur IA")
    elif langage == "java":
        print("Tu es un développeur logiciel")
    elif langage == "reactjs":
        print("Tu es un développeur mobile")
    else:
        print("Un jour, je serai le meilleur développeur...")


determiner_specialisation("JavaScript")  
determiner_specialisation("python")  
determiner_specialisation("java")  
determiner_specialisation("reactjs")  
determiner_specialisation("C#")  
